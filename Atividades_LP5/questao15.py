# Questao 15: Escreva um programa que pergunte ao usuário uma idade e verifique se a pessoa é adolescente (entre 13 e 17 anos).

idade = int(input('Digite sua idade:'))

if idade >=13 and idade <= 17:
    print('Você é um adolescente.')
elif idade <=12:
    print('Você é uma criança. cuidado com o Felca')
elif idade >=18:
    print('Você ja é adulto.')






































