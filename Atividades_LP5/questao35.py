# Questao 35: Desenvolva um algoritmo que peça ao usuário para digitar dois números e verifique se a multiplicação deles é igual a 20.

numero1 = int(input('Informe o primeiro numero:'))
numero2 = int(input('Informe o segundo numero:'))

mult = numero1 * numero2 

if mult == 20:
    print('O resultado da multiplicação destes dois numeros é igual a 20')
else:
    print('O resultado da multiplicação deste numeros é diferente de 20')























































