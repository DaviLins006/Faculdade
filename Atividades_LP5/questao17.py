# Questao 17: Crie um programa que solicite ao usuário dois números e exiba a soma, subtração, multiplicação e divisão entre eles.

numero1 = int(input('Informe o primeiro numero:'))
numero2 = int(input('Informe o segundo numero:'))

soma = numero1 + numero2
sub = numero1 - numero2
div = numero1 / numero2
mult = numero1 * numero2

print(f'Os numeros somados resultam em {soma}')
print(f'Os numeros subtraidos resultam em {sub}')
print(f'Os numeros dividos resultam em {div}')
print(f'Os numeros multiplicados resultam em {mult}')

























































