# Questao 25: Escreva um programa que peça ao usuário um número de 0 a 20 e verifique se ele está entre 10 e 15

numero = int(input('Digite um numero de 0 a 20:'))

if numero >= 10 and numero <= 15:
    print(f'{numero} está entre 10 e 15')
else:
    print(f'{numero} não está entre 10 e 15') 













































