# Questao 37: Desenvolva um algoritmo que peça ao usuário para digitar um número e verifique se ele é múltiplo de 2 ou de 5.

numero = int(input('Digite um numero:'))

if numero % 3 == 0:
    print(f'{numero} é multiplo de 3')
elif numero % 5 == 0:
    print(f'{numero} é multiplo de 5')
else:
    print(f'{numero} não é multiplo de 3 ou de 5')


























































