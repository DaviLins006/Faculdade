# Questao 53: Escreva um programa que peça ao usuário um número e exiba a contagem regressiva desse número até 1.


numero = int(input('Digite um numero para a contagem regressiva:'))
print(f'Contagem regressiva de {numero} até 1:')

for i in range(numero, 0, -1):
    print(i, end=' ')                  



















































