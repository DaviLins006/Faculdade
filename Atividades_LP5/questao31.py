# Questao 31: Escreva um algoritmo que peça ao usuário um número de 1 a 5 e verifique se ele é igual a 3.

numero = int(input('Digite um numero de 1 a 5:'))

if numero == 3:
    print('Seu numero é igual a três')
elif numero > 5:
    print('Digite um numero entre 1 a 5.')
else:
    print('Seu numero não é igual a três')


































