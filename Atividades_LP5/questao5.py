# Questão 5: Faça um programa que solicite ao usuário dois números e verifique se ambos são pares.

numero1 = int(input('Escolha o número 1: '))
numero2 = int(input('Escolha o número 2: '))




if numero1 % 2 == 0 and numero2 % 2 == 0:
    print('Ambos os numeros são pares')
elif numero1 % 2 and numero2 % 2:
    print('Ambos os numeros NÃO são pares')
else:
    print('Um dos numeros Não é par')



































