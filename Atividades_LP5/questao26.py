# Questao 26: Desenvolva um algoritmo que peça ao usuário para inserir dois números e verifique se ambos são múltiplos de 5.

numero1 = int(input('Escolha o número 1: '))
numero2 = int(input('Escolha o número 2: '))

if numero1 % 5 == 0 and numero2 % 5 == 0:
    print('Ambos os numeros são multiplos de 5')
else:
    print('Um dos numeros ou ambos não são multiplos de 5')


























































