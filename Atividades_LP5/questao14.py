# Questao 14: Desenvolva um algoritmo que peça ao usuário para digitar dois números e verifique se a soma deles é maior que 100.

numero1 = int(input('Informe o primeiro numero:'))
numero2 = int(input('Informe o segundo numero:'))

soma = numero1 + numero2

if soma >= 100:
    print(f'O resultado é {soma} um numero maior que 100')
elif soma <= 99:
    print(f'O resultado é {soma} um numero menor que 100')








































