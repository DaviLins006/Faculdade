# Questao 38: Escreva um programa que peça ao usuário para digitar sua altura em metros e verifique se ela é maior que 1.75


altura = float(input('Digite aqui sua altura:'))

if altura > 1.75:
    print(f"Olha so você tem {altura} é maior que 1.75")
else:
    print(f"Você tem {altura} é menor que 1.75")






























