# questao 63: Desenvolva um algoritmo que peça ao usuário para inserir 5 números, adicione-os a uma lista e, depois, exiba a soma de todos os números na lista.

numeros = []
for i in range(5):
    num = float(input("Digite um número: "))
    numeros.append(num)
soma = sum(numeros)
print(f"A soma de todos números  é: {soma}")




























































